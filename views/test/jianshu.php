                    <p>赏花赏月赏生活，学诗学词学文章，且为君狂，且为才倾。沾衣欲湿杏花雨，吹面不寒风雅颂。</p>
                    <p><i><br></i></p>
                    <p><i>《春江花月夜 》 张若虚</i><br></p>
                    <p><i>春江潮水连海平，海上明月共潮生。 滟滟随波千万里，何处春江无月明！ 江流宛转绕芳甸，月照花林皆似霰。 空里流霜不觉飞，汀上白沙看不见。 江天一色无纤尘，皎皎空中孤月轮。 江畔何人初见月？江月何年初照人？ 人生代代无穷已，江月年年望相似。</i>
                    </p>
                    <p><i>不知江月待何人，但见长江送流水。 白云一片去悠悠，青枫浦上不胜愁。 谁家今夜扁舟子？何处相思明月楼？ 可怜楼上月徘徊，应照离人妆镜台。 玉户帘中卷不去，捣衣砧上拂还来。 此时相望不相闻，愿逐月华流照君。 鸿雁长飞光不度，鱼龙潜跃水成文。
                            昨夜闲潭梦落花，可怜春半不还家。 江水流春去欲尽，江潭落月复西斜。 斜月沉沉藏海雾，碣石潇湘无限路。 不知乘月几人归，落月摇情满江树。</i></p>
                    <p><br></p>
                    <p>　《全唐诗》中存诗仅两首的张若虚，在唐代灿如繁星的诗人群里实在毫不起眼，然而真正爱国学、爱唐诗宋词的人，想必都知道张若虚及其著名诗篇《春江花月夜》 。　　</p>
                    <p>把大唐引进诗歌朝代的，也许正是张若虚与他的《春江花月夜》。

                        　　</p>
                    <p>
                        闻一多先生曾给这首诗以极高的评价：“在这种诗面前，一切的赞叹是饶舌，几乎是亵渎。”又说“这是诗中的诗，顶峰上的顶峰。从这边回头一望，连刘希夷都是过程了，不用说卢照邻和他的配角骆宾王，更是过程的过程。”说张若虚与他的《春江花月夜》“和另一个顶峰陈子昂分工合作，清除了盛唐的路——张若虚的功绩是无可估计的。”

                        　　</p>
                    <p>的确，“孤篇横绝，竟为大家”绝不是戏言。尽管这一荣誉称号来得太晚太突然，谜一样的诗和迷一样的诗人最终还是成全了我们的期待和想象。</p>
                    <p>《春江花月夜》既富于南方民歌的色彩与风调，又较成功地运用了经过齐梁到唐初百年酝酿接近完成的新诗格律，还首次探索了七言诗中以小组转韵结合长篇的技巧，三者的糅合是那样完美，给后来的诗人提供了一个很好的范本。这理所当然是个顶峰。

                        　</p>
                    <p>
                        　《春江花月夜》的章法结构，以整齐为基调，以错杂显变化。三十六行诗，共分为九组，每四句一小组，一组三韵，另一组必定转用另一韵，象九首绝句。这是它整齐的一面。它的错综复杂，则体现在九个韵脚的平仄变化。开头一、三组用平韵，二、四组用仄韵，随后五六七八组皆用平韵，最后用仄韵结束，错落穿插，声调整齐而不呆板。在句式上，大量使用排比句、对偶句和流水对，起承转合皆妙，文章气韵无穷。诗中春、江、花、月、夜、人几个主题词错落重叠，伸缩变化，把读者引进了一个目眩五彩、浑然忘我的境界。</p>
                    <p>诗中江与月这两个主题中的主题被反复拓展，不断深化。春江、江流、江天、江畔、江水、江潭、江树这纷繁的形与景，和着明月、孤月、江月、初月、落月、月楼、月华、月明复杂的光与色，并通过与春、夜、花、人的巧妙结合，构成了一幅色美情浓斑斓迷离的春江夜月图。<br>
                    </p>
                    <p>
                        诗人没有局限于一轮江月，而是把一种复杂的人类情感贯穿始终。无论是初月的明媚、高月的皎洁还是斜月与落月的迷离缠绵，抑或楼头月的徘徊、镜中月的清影、帘内月的倾注、砧上月的流照，无一不打上情感的烙印。把一轮明月写到如此清雅且夺人心魄的地步，就不仅仅是传世之作、而应该是旷世之作了。</p>
                    <p>好吧，我们就不要打扰这首诗的意境了，只谈一些主观感受就很好。<br></p>
                    <p>这首诗，就像是青春的初唐和大自然谈了一场恋爱 。《春江花月夜》为唐诗开创了一个另类的题材和情感风貌，它那元气淋漓的意象和多愁善感的激情，就像是青春的初唐，在夜深人静时，和万物生长的大自然谈了一场恋爱。
                        虽然这场恋爱的结果，使得初唐释放了青春的荷尔蒙，但也使它那华丽的青春初尝了感伤的滋味。
                        因为这首诗的内容是表现人和自然的关系，探讨的又是短暂与永恒的哲学命题，因而在它那华丽的青春外壳里，注定会隐藏着终极的感伤。虽然，这感伤不是关于朝气蓬勃的初唐时代的，而是关于人类、人生那超越时空、亘古未变的命运的。这也正是张若虚这个另类诗人的独特价值。</p>
                    <p> 生活在七世纪初叶的张若虚，从前人从未有过的角度探讨宇宙的存在，又以永恒的宇宙为参照，来反观人类的命运。
                        他从自然的永恒、无限，联想到人生的短促、无常，把人与自然的关系上升到哲学高度，发出了“人生代代无穷已，江月年年只相似”这样的终极关怀式的慨叹，既带着无可奈何的伤感和迷惘的况味，又哀而不伤地表达了青春的梦幻和人生的绚烂。就像他的名字一样，他的愁苦是一种虚幻的愁苦，有点“为赋新词强说愁”的味道。
                        由此，他向读者展现了唐诗表现题材的另一面，也由此树立了仅属于他自己的独特标志。 </p>
                    <p>写完《春江花月夜》，张若虚匆匆熄灭了人生的踪迹，却点亮了唐诗的星空 。真实的张若虚到底是怎么样的一个人？在初唐那个大时代里，他都度过了怎么样的一生，还有没有建立其他功业？
                        因为生平在史书上缺席，人们便只能想象这位伟大诗人的人生故事，想象一个传奇中他与辛夷的生死绝恋。他用近千年的埋没，换来辛夷花和扬州月亮的前世今生。从此以后，扬州的月亮就成了中国最著名的月亮
                        。如姜夔的“二十四桥仍在，波心荡，冷月无声”，譬如杜牧的“二十四桥明月夜，玉人何处教吹箫”…… 因为张若虚从诗歌的角度对中国
                        “天—人”观念的哲学建构，实在有着有非常重大的意义。以至于，苏东坡那首脍炙人口的“明月几时有，把酒问青天”就明显携带着从《春江花月夜》继承过来的基因。正是因为如此，《春江花月夜》在中国诗歌发展史上所起的作用，就远远不是一首杰作那么简单。 </p>
                    <p>
                        自《诗经》至张若虚，其间一千几百年，没人把一轮江月写得如此凄美多情。在诗歌的表现形式上，南朝民歌和齐梁声律学，经过多年的酝酿发展，到了张若虚手里，恍如金丹炼成突现奇光，语言声律与形式技巧以及描篇布局，被那样的完美地糅合在一起。在诗歌发展还找不到前进方向的唐前期，《春江花月夜》本身就如同光耀千古的一轮高天朗月，照亮了盛唐的路，催生了诗国的灿烂。而张若虚之后，又是一千多年过去了，仍然无人能把一夕江月渲染得这般淋漓尽致，历尽沧桑变幻，诗篇不朽而江月依旧。<br>
                    </p>
                    <p>赏析/黎峰</p>
